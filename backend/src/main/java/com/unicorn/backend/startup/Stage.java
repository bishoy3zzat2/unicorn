package com.unicorn.backend.startup;

/**
 * Enum representing the development stage of a startup.
 */
public enum Stage {
    IDEA, // Initial concept phase
    MVP, // Minimum Viable Product developed
    SEED, // Seed funding stage
    SERIES_A, // Series A funding stage
    SERIES_B, // Series B funding stage
    GROWTH, // Active growth and scaling
    SCALING, // Advanced scaling operations
    ALL // Open to all stages
}
